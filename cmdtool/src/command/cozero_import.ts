export default async function cmd_cozero_import() {
  const importData: ImportData = {
    functions: INDUSTRIES.map(co2MpcFunction),
    datasets: COUNTRIES.map(co2CountryDataset).concat(
      INDUSTRIES.map(co2IndustryDataset)
    ),
  };

  await do_import(importData);
  console.log("Cozero data import finished");
}

function co2MpcFunction(industry: string): FunctionDeclaration {
  return {
    id: `cozero-co2-${industry}`,
    inputs: ["employees", "electricity_mix"],
    outputs: ["co2"],
    inputMatrix: [[300, 50]],
  };
}

function co2CountryDataset(countryCode: string): Dataset {
  return {
    id: `cozero-country-${countryCode}`,
    dimensions: [
      {
        id: "co2",
        integerValues: [10000, 20000, 30000, 100000, 200000, 300000],
      },
    ],
  };
}

function co2IndustryDataset(industry: string): Dataset {
  return {
    id: `cozero-industry-${industry}`,
    dimensions: [
      {
        id: "co2",
        integerValues: [20000, 30000, 100000, 200000, 300000],
      },
    ],
  };
}
